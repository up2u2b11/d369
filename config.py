def compute_special_6(text: str) -> int:
    """الحساب الخاص-6 — تُجمع القيم كأعداد عشرية"""
    import re
    clean = re.sub(r'[\u0610-\u061A\u064B-\u065F\u0670\u06D6-\u06ED]', '', text)
    total = 0
    for ch in clean:
        total += SPECIAL_6_MAP.get(ch, 0)
    return total


